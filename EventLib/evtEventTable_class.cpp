// Constructor.

EventTable::EventTable()
{
   reset();
}

//******************************************************************************
//******************************************************************************
//******************************************************************************

void EventTable::reset()
{
   for (int i = 0; i < cEvt_MaxNumEvents; i++)
   {
      mArray[i].reset();
   }
}

//******************************************************************************
//******************************************************************************
//******************************************************************************

void EventTable::showRecord(int aEvtId, int aPF)
{
   mArray[aEvtId].show(aPF);
}

